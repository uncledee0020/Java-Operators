public class Decrement {
    public static void main(String[] args){
        // --i, i--, i=i-1
        int i=5;
        System.out.println(i);
        int a=i--;  //a=5, i=4
        int b=--i; //i=3, b=3
        System.out.println(i);
        System.out.println(a);
        System.out.println(b);
    }
    //
}
