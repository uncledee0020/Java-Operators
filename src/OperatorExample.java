public class OperatorExample {
    public static void main(String[] args) {
        int x = 3;
        int y = 3;
        System.out.println("x is " + x + " and y is " + y);
        System.out.println("x == y is " + (x == y)); // == operator
        System.out.println("x!=y is " + (x != y));  // != operator
        System.out.println("x > y is " + (x > y)); // > operator
        System.out.println("x < y is " + (x < y)); // < operator
        System.out.println("x >= y is " + (x >= y)); // >= operator
        System.out.println("x <= y " + (x <= y)); // <= operator

    }
}
